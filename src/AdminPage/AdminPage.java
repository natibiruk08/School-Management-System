    public void loadData(String like) throws SQLException{
        DefaultTableModel model=(DefaultTableModel) listTable.getModel();
            conn=DriverManager.getConnection(url,usr,pass);
            stmnt=conn.createStatement();
        int count=model.getRowCount();
        for(int i=0;i<count;i++){
            model.removeRow(0);
        }
        
        if(like.equals("")){
            rs=stmnt.executeQuery("select * from student");
        }
        else{
            rs=stmnt.executeQuery("select * from student where Fname like \""+like+"%\"");
        }
        while(rs.next()){
                String[] data=new String[11];
                data[0]=Integer.toString(rs.getInt("id"));
                data[1]=rs.getString("Fname");
                data[2]=rs.getString("Mname");
                data[3]=rs.getString("Lname");
                data[4]=rs.getString("sex");
                data[5]=Integer.toString(rs.getInt("age"));
                data[6]=rs.getString("username");
                data[7]=rs.getString("password");
                data[8]=Integer.toString(rs.getInt("academic_year"));
                data[9]=rs.getString("address");
                data[10]=Integer.toString(rs.getInt("class_id"));
                model.addRow(data);
            }
        conn.close();
    }
